# Create add function w/ 2 parameters
def add (a,b):
    return a + b

# Create subtract function w/ 2 parameters
def subtract (a,b):
    return a-b

# Create divide function w/ 2 parameters
def divide (a,b):
    return a/b

# Call seperate print statement for each function
print(add(1,2))
print(subtract(4,1))
print(divide(8,2))
